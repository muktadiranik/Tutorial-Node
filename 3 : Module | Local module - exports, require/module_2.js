const subtractTwoNumbers = (a, b) => {
  return `${a} - ${b} = ${a - b}`;
};

const devideTwoNumbers = (a, b) => {
  return `${a} / ${b} = ${a / b}`;
};

const multiplyTwoNumbers = (a, b) => {
  return `${a} * ${b} = ${a * b}`;
};

module.exports = {
  subtractTwoNumbers,
  devideTwoNumbers,
  multiplyTwoNumbers,
};
